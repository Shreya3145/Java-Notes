package Patterns;

public class Pattern4 {
        public static void main(String[] args) {
            for (int i = 1; i <= 5; i++) {
            System.out.print(i + " "); 
            System.out.println();
            }
    
            for (int i = 1; i <= 5; i++) {
            System.out.print(2 * i + 1 + " "); 
            System.out.println();
            }
    
            for (int i = 1; i <= 5; i++) {
            System.out.print(3 * i + " "); 
            System.out.println();
            }

    
            for (int i = 1; i <= 5; i++) {
            System.out.print(4 * i + 1 + " "); 
            System.out.println();
            }
    
            for (int i = 1; i <= 5; i++) {
            System.out.print(5 * i + " "); 
            System.out.println();
            }
        }
    }
